    public void motorBikesDisplay(){
        
        model.setRowCount(0);
        
        ArrayList<MotorBike> motorBikes=new ArrayList<>();
        motorBikes=db.getAllMotorBikes();
        
        for(MotorBike m:motorBikes){
            String hasGrenaj;
            if(m.getHasGrenaj())
                hasGrenaj="yes";
            else
                hasGrenaj="no";
            Object[] add={m.getId(),m.getBrand(),m.getEngineVolume(),m.getTireNumber(),hasGrenaj,m.getPrice()};
            model.addRow(add);
        }
    }
