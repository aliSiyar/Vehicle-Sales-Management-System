    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        //buy 
        
        int selectedRow=motorbikeTable.getSelectedRow();//delete
        
        if(selectedRow==-1){
            if(model.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "wait for new cars to be added to buy motorbikes.");
                
            }
            else{
                JOptionPane.showMessageDialog(this, "select motorbike to buy");
            }
        }
        else{
            int id=(int) model.getValueAt(selectedRow, 0);
            int price=(int) model.getValueAt(selectedRow, 5);
            if(user.getRemainder()>=price){
                
                user.setRemainder(user.getRemainder()-price);
                db.updateRemainder(user.getId(), user.getRemainder());
                jLabel1.setText("Your remainder: "+user.getRemainder());
                db.deleteMotoBike(id);
            }
            else{
                JOptionPane.showMessageDialog(this, "you don't have enough money to buy a motorbike");
            }
            
            
            motorBikesDisplay();
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed
    }
